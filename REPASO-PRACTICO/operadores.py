#OPERADORES ARITMETICOS
a = 10
b = 3

suma = a + b            # Suma
resta = a - b           # Resta
multiplicacion = a * b   # Multiplicación
division = a / b        # División
division_entera = a // b # División entera
modulo = a % b          # Módulo
potencia = a ** b       # Potencia

print("Suma:", suma)
print("Resta:", resta)      
print("Multiplicación:", multiplicacion)
print("División:", division)
print("División entera:", division_entera)
print("Módulo:", modulo)
print("Potencia:", potencia)


#OPERADORES DE ASIGNACION
x = 5
x += 3  # Equivalente a x = x + 3
x -= 2  # Equivalente a x = x - 2
x *= 4  # Equivalente a x = x * 4
x /= 2  # Equivalente a x = x / 2
x %= 3  # Equivalente a x = x % 3
x **= 2 # Equivalente a x = x ** 2
x //= 2 # Equivalente a x = x // 2

#OPERADORES DE COMPARACION
a = 10
b = 20
print(a == b)  # Igualdad
print(a != b)  # Desigualdad    
print(a > b)   # Mayor que
print(a < b)   # Menor que      
print(a >= b)  # Mayor o igual que
print(a <= b)  # Menor o igual que

#OPERADORES LOGICOS
x = True
y = False
print(x and y)  # AND lógico
print(x or y)   # OR lógico 
print(not x)     # NOT lógico

