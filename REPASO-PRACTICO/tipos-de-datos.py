# str cadena de caracteres de longitud variable
nombre = "Juan"

# int numero entero
edad = 30       

# float numero decimal
altura = 1.75   

# bool valor booleano (True o False)
es_estudiante = True

# list lista de elementos
frutas = ["manzana", "banana", "cereza"]

# dict diccionario de pares clave-valor
persona = {"nombre": "Ana", "edad": 25}

# tuple tupla de elementos inmutables
coordenadas = (10.0, 20.0)

# set conjunto de elementos Ãºnicos
numeros = {1, 2, 3, 4, 5}

# None valor nulo
valor_desconocido = None

# Imprimir los tipos de datos
print(type(nombre))          # <class 'str'>
print(type(edad))            # <class 'int'>
print(type(altura))          # <class 'float'>
print(type(es_estudiante))   # <class 'bool'>
print(type(frutas))          # <class 'list'>
print(type(persona))         # <class 'dict'>
print(type(coordenadas))     # <class 'tuple'>
print(type(numeros))         # <class 'set'>
print(type(valor_desconocido)) # <class 'NoneType'>




